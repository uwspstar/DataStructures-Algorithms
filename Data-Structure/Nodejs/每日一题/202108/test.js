var isValidSudoku = function (board) {
    let map = {};
    let m = board.length;
    for (let i = 0; i < m; i++) {
        for (let j = 0; j < m; j++) {
            let tmp = board[i][j];
            if (tmp === '.') continue; 

            if (map['row_' + i + '_' + tmp]) {
                return false;
            } else {
                map['row_' + i + '_' + tmp] = true;
            }
            if (map['col' + j + '_' + tmp]) {
                return false;
            } else {
                map['col_' + j + '_' + tmp] = true;
            }
            if (map['block' + '_' + Math.floor(i / 3) + '_' + Math.floor(j / 3) + '_' + tmp]) {
                return false;
            } else {
                map['block' + '_' + Math.floor(i / 3) + '_' + Math.floor(j / 3) + '_' + tmp] = true;
            }
        }
    }
    console.log('map=',map)
    return true;
};

